export function buildLessonImages(pool: string[], count: number) {
  const safeCount = Math.max(1, Math.min(count, pool.length));
  return pool.slice(0, safeCount).map((url, index) => ({ order: index + 1, url }));
}

export function normalizeLessonImages(images: { order: number; url: string }[]) {
  return [...images]
    .sort((a, b) => a.order - b.order)
    .map((image, index) => ({ order: index + 1, url: image.url }));
}

